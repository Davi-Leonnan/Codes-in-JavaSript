// Const: usado para variáveis cujo o valor não muda. ("Const": Constante)

// Por via das dúvidas, podemos colocar o nome das variáveis tudo em maiúsculo.

const AGE = 90;

// let: Usado para variáveis cuso valor pode ser mudado.

let hair = brown;

hair = blond;

// console.log(): Permite ao computador exibir um valor no concole.

console.log("Hello World!");

const name = "Maria";
console.log("name");

// Adicionar "+" junto com uma string.

const numberOfFollowers = "55"
console.log("Followers" + numberOfFollowers);

const numberOfLikes = 5 + 10;
console.log(numberOfLikes + "likes");

// "true" and "false".

// Não tem as aspas nos valores e não é um valor numérico. Mostrando se um valor está ativado ou desativado.

const Name = "David";
const Acess = true;
console.log(Acess);

// Uma "!" seguida de "true" resulta em uma variável falsa, e vice versa.

const fruit = "tomato";
const vegetables = !true;
console.log(vegetables);

// Podemos usar "<" para comparador um número menor que o outo.

console.log(1 < 90);

// Podemos usar ">" para comparador um número maior que o outo.

console.log(100 > 50);

// Podemos usar "<=" para comparador um número menor ou igual que o outo.

console.log(62 <= 106);

// Podemos usar ">=" para comparador um número maior ou igual que o outo.

console.log(75 >= 70);

// Usamos a palavra-chave "if" para condicionar uma instrução:
// Obs: as condicionais vem sempre acompanhada entre parênteses "()".
// Obs2: As condicionais não definem a execução de todo o código, e sim de apenas um bloco dele, o qual vem acompanhado entre chaves "{}".

if (true) {
    console.log("Hello!");
}

// Caso usamos a condicional "false", ela irá simplesmente pular a linha de código:

if (flase) {
    console.log("Hello!")
}

const toy = true;
if (toy) {
    console.log("Watch Toy Story!");
}

