// Armazenando Valores:


// No JavaScript, Criar uma variável significa "Declarar" uma variável.

// "Strings" são sequências de caracteres alfa-numéricos.

// Criação de uma variável: var x = "Nome da variável";


// Exemplo 1: Printando uma variável

var x = "Batata";

document.write(x);


// Criação de valores booleanos (Verdadeiro ou Falso): var JS_sucks = false ou var JS_rocks = true;

// sinais booleanos (true, false) sempre são representados sem as àspas.


// Exemplo 2: Armazenar a idade em uma variável:

var age = 20;

document.write(age);

// OBS 02: Números podem ser escritos tanto inteiros quanto deciamis.


// Exemplo 3:

var Remo_é_o_melhor = true;

var Paysandu_é_o_melhor = false;

document.write(Remo_é_o_melhor);
document.write(Paysandu_é_o_melhor);