// Fazendo Comparações:


// Obs: Assim como na linguagem Python, o simbolo "=" serve para declarar valores de variáveis, enquanto que o "==" serve para comparar duas variáveis distintas.

// Obs 02: O simbolo "!=" serve para declarar se duas váriaveis possuem valores diferentes ou não.

// Obs 03: O simbolo "&&" é usado quando se tem dois ou mais valores a serem analisados.

// Obs 04: O simbolo "||" é usado quando apenas uma das condições analisadas necessita ser verdadeira.

// Exemplo 1: Comparações de especificações de celulares.
// mp: megapixels  // gb: gigabytes

var mp =  5;

var gb = 16;

mp > 5 && gb > 16;

mp >10 || gb > 16;